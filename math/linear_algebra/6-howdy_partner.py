#!/usr/bin/env python3
"""Module that concatenates two arrays"""


def cat_arrays(arr1, arr2):
    """Concatenates two arrays
    Args:
        arr1: The first array (list of ints/floats)
        arr2: The second array (list of ints/floats)
    Returns:
        A new list containing elements of arr1 followed by arr2
    """
    return arr1 + arr2
